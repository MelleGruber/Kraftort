export function getStoriesBySeason(season: string): { locationId: string, story: any }[] {
  const stories: { locationId: string, story: any }[] = [];
  
  sanctuaryData.locations.forEach(location => {
    location.stories.forEach(story => {
      if (story.season === season) {
        stories.push({ locationId: location.id, story });
      }
    });
  });
  
  return stories;
}
